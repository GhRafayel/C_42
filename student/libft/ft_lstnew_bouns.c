#include "libft.h"

t_list	*ft_lstnew(void *value)
{
	t_list	*new_node;

	new_node = (t_list *)malloc(sizeof(t_list));
	if (new_node == NULL)
	{
		return (NULL);
	}
	new_node -> content = value;
	new_node -> next = NULL;
	return (new_node);
}
